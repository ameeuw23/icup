<?php
namespace ICup\Bundle\PublicSiteBundle\Entity;

/**
 * Class TeamInfo
 * @package ICup\Bundle\PublicSiteBundle\Entity
 */
class TeamInfo {

    /**
     * @var mixed $id
     */
    public $id;
    /**
     * @var mixed $club
     */
    public $club;
    /**
     * @var mixed $name
     */
    public $name;
    /**
     * @var mixed $country
     */
    public $country;
    /**
     * @var mixed $group
     */
    public $group;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getClub()
    {
        return $this->club;
    }

    /**
     * @param mixed $club
     */
    public function setClub($club)
    {
        $this->club = $club;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return mixed
     */
    public function getCountry()
    {
        return $this->country;
    }

    /**
     * @param mixed $country
     */
    public function setCountry($country)
    {
        $this->country = $country;
    }

    /**
     * @return mixed
     */
    public function getGroup()
    {
        return $this->group;
    }

    /**
     * @param mixed $group
     */
    public function setGroup($group)
    {
        $this->group = $group;
    }
}